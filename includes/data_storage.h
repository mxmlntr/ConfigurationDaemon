struct CRC {
    boost::crc_32_type crc;
    void operator()(string_view s)
    {
        crc.process_bytes(s.data(), s.size());
    }
    template <typename T, typename = std::enable_if_t<is_integral_v<T>>>
    void operator()(T const& i)
    {
        static_assert(is_trivial_v<T>);
        static_assert(not is_class_v<T>);
        crc.process_bytes(&i, sizeof(i));
    }

    auto get() { return crc.checksum(); }
};

